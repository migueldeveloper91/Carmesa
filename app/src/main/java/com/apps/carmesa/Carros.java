package com.apps.carmesa;

public class Carros {
    private String placa;
    private String usuarioCarro;
    private String modelo;
    private String marca;
    private String color;

    public Carros() {
        this.placa = placa;
        this.usuarioCarro = usuarioCarro;
        this.modelo = modelo;
        this.marca = marca;
        this.color = color;
    }


    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getUsuarioCarro() {
        return usuarioCarro;
    }

    public void setUsuarioCarro(String usuarioCarro) {
        this.usuarioCarro = usuarioCarro;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
